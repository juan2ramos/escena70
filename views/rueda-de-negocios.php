        <h3>RUEDA DE NEGOCIOS ESCENA 70</h3>
    <p>
        La Rueda de Negocios de ESCENA 70 será un ambiente propicio para promover encuentros entre programadores, instituciones, festivales y organizaciones que se entrevistarán para realizar negocios, desarrollar relaciones asociativas o alianzas estratégicas. A través de un mecanismo simple y eficiente de reuniones directas y planificadas se coordinarán la oferta y la demanda de productos culturales, ayudando a los participantes a identificar y seleccionar aquellas contrapartes con las que podrán hacer negocios. Unos de los objetivos más importantes es ampliar la base de contactos y conocer empresarios del sector de las artes escénicas que se encontrarán durante tres días en la ciudad de Buenos Aires.
    </p>

        <h3>Cómo se beneficiarán los participantes </h3>
        <p>
        - Se generarán contactos directos con un número considerable de programadores en un mismo lugar, lo cual generará ahorros en tiempo y en costos logísticos.
            <br/>
        - Se establecerán alianzas o intercambios ventajosos en comparación con otros medios de contacto.
            <br/>
        - Se tendrá opción de entrar en contacto con empresas con las que posiblemente de otra forma sería muy difícil o imposible.
            <br/>
        - Para actores y productores nuevos, será una buena forma de introducirse al circuito escénico a nivel internacional.
        </p>


        <h3> Etapas de la Rueda de Negocios </h3>
        <p>
        Pre-Rueda: Es la primera etapa, en la cual se realiza la inscripción, la organización de citas y la preparación de todos los materiales que van a llevar los artistas a la rueda. En el caso de Escena 70 incluirá también la capacitación de los participantes.

        En Rueda: Esta etapa corresponde al desarrollo del evento, o sea, la participación en las rondas de negociaciones entre oferentes y demandantes.

        Post-Rueda: Son las actividades que se llevan a cabo después del evento y se enfoca en el seguimiento a contactos realizados, la evaluación y el monitoreo para medir el impacto de la rueda.
        </p>


